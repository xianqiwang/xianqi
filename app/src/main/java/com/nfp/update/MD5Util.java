package com.nfp.update;

import java.io.File;
import java.io.FileInputStream;
import java.io.FileNotFoundException;
import java.io.IOException;
import java.math.BigInteger;
import java.nio.MappedByteBuffer;
import java.nio.channels.FileChannel;
import java.security.MessageDigest;

public class MD5Util {
    /**
     * 生成md5
     *
     * @param message
     * @return
     */
    public static String getMD5(String message) {
        String md5str = "";
        try {
            // 1 创建一个提供信息摘要算法的对象，初始化为md5算法对象
            java.security.MessageDigest md = java.security.MessageDigest.getInstance("MD5");

            // 2 将消息变成byte数组
            byte[] input = message.getBytes("UTF-8");

            // 3 计算后获得字节数组,这就是那128位了
            byte[] buff = md.digest(input);

            // 4 把数组每一字节（一个字节占八位）换成16进制连成md5字符串
            md5str = bytesToHex(buff);

        } catch (Exception e) {
            e.printStackTrace();
        }
        return md5str;
    }

    /**
     * 二进制转十六进制
     *
     * @param bytes
     * @return
     */
    public static String bytesToHex(byte[] bytes) {
        StringBuffer md5str = new StringBuffer();
        // 把数组每一字节换成16进制连成md5字符串
        int digital;
        for (int i = 0; i < bytes.length; i++) {
            digital = bytes[i];

            if (digital < 0) {
                digital += 256;
            }
            if (digital < 16) {
                md5str.append("0");
            }
            md5str.append(Integer.toHexString(digital));
        }
        return md5str.toString();//.toUpperCase();
    }
    public static String getMd5ByFile(java.io.File file) throws java.io.FileNotFoundException {
        String value = null;
        java.io.FileInputStream in = new java.io.FileInputStream(file);
        try {
            java.nio.MappedByteBuffer byteBuffer = in.getChannel().map(java.nio.channels.FileChannel.MapMode.READ_ONLY, 0, file.length());
            java.security.MessageDigest md5 = java.security.MessageDigest.getInstance("MD5");
            md5.update(byteBuffer);
            java.math.BigInteger bi = new java.math.BigInteger(1, md5.digest());
            value = bi.toString(16);
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if(null != in) {
                try {
                    in.close();
                } catch (java.io.IOException e) {
                    e.printStackTrace();
                }
            }
        }
        return value;
    }
	public  static boolean chmod755(String filePath){
		
		if(filePath == null || filePath.equals("")){
			return false;
		}
		
		try {
			String command = "chmod 755 "+ filePath;
			Runtime runtime = Runtime.getRuntime();
			runtime.exec(command);
		} catch (java.io.IOException e) {
			e.printStackTrace();
			return false;
		}
		
		return true;
	}
}
